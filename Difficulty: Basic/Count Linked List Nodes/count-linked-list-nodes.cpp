class Solution {
  public:
    // Function to count the number of nodes in a linked list.
    int getCount(struct Node* head) {
        // Base case: if the head is null, the list is empty, so return 0.
        if (head == nullptr) {
            return 0;
        }

        // Recursive case: count the current node (1) and add the count of the rest of the list.
        return 1 + getCount(head->next);
    }
};

